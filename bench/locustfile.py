import random
from locust import HttpUser, task, between

SAMPLE_TEXTS = [
    "Без труда не выловишь и рыбку из пруда.",
    "Тише едешь — дальше будешь.",
    "Не всё то золото, что блестит.",
    "Семь раз отмерь, один раз отрежь.",
    "Слово не воробей, вылетит — не поймаешь.",
    "За двумя зайцами погонишься — ни одного не поймаешь.",
    "Готовь сани летом, а телегу зимой, потому что кто рано встаёт, тому бог подаёт.",
    "Не имей сто рублей, а имей сто друзей, ведь друг познаётся в беде, а настоящая дружба дороже любого золота.",
    "Москва не сразу строилась, и даже самый длинный путь начинается с первого шага, поэтому терпение и труд всё перетрут.",
    "Яблоко от яблони недалеко падает, но при этом каждый кузнец своего счастья, и как потопаешь, так и полопаешь, а без труда не выловишь и рыбку из пруда.",
    "Век живи — век учись, а дураком помрёшь, но при этом учёного учить — только портить, хотя ученье — свет, а неученье — тьма, и грамоте учиться — всегда пригодится, ведь знание — сила, а сила — в правде, и кто владеет знанием, тот владеет миром, потому что мудрость приходит с годами, а годы летят быстро, и нужно ценить каждый день, ибо утро вечера мудренее, а поутру все дела спорятся лучше.",
]

class EmbeddingUser(HttpUser):
    wait_time = between(0.1, 0.5)

    @task
    def embed_text(self):
        text = random.choice(SAMPLE_TEXTS)
        self.client.post("/embed", json={"text": text})

    @task(1)
    def health_check(self):
        self.client.get("/health")
